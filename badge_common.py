# Text Parsing with :icon: Syntax
# =============================================================================

class BadgeTextParser:
    """
    Parses text with embedded icon references using :icon: syntax.

    Supported syntax:
    - ::  -> literal colon
    - :N: -> reference to preloaded image N (where N is a number)
    - :iconname: -> reference to builtin icon (e.g., :heart:, :happy:)
    - :path/to/image.png: -> loads image file and creates reference

    Example: "Hello :heart: World" or "Logo :gfx/logo.png: here"
    """

    def __init__(self):
        self.bitmap_preloaded = [([], 0)]  # Index 0 is unused
        self.bitmaps_preloaded_unused = False

    def add_preload_img(self, filename):
        """Preload an image file for later use."""
        self.bitmap_preloaded.append(self.bitmap_img(filename))
        self.bitmaps_preloaded_unused = True

    def are_preloaded_unused(self):
        """Check if any preloaded images were not used."""
        return self.bitmaps_preloaded_unused is True

    @staticmethod
    def get_named_bitmap_keys():
        """Return list of available builtin icon names."""
        return list(BITMAP_NAMED.keys())

    def bitmap_char(self, ch):
        """
        Returns a tuple of (buffer, length_in_byte_columns) for given character.

        For regular characters, returns 11 bytes from the font.
        For control characters (icons), returns the icon bitmap data.
        """
        if ord(ch) < 32:
            if ch in BITMAP_BUILTIN:
                return BITMAP_BUILTIN[ch][:2]

            self.bitmaps_preloaded_unused = False
            return self.bitmap_preloaded[ord(ch)]

        if ch in CHAR_OFFSETS:
            o = CHAR_OFFSETS[ch]
            return FONT_11X44[o:o + 11], 1

        # Unknown character - return space
        o = CHAR_OFFSETS.get(' ', 0)
        return FONT_11X44[o:o + 11], 1

    def parse_text(self, text):
        """
        Parse text with :icon: syntax and return bitmap data.

        Returns: tuple of (buffer, length_in_byte_columns)

        Processing:
        - "::" is replaced with a single ":"
        - ":1:" is replaced with CTRL-A referencing the first preloaded image
        - ":happy:" is replaced with a reference to builtin smiley glyph
        - ":gfx/logo.png:" preloads the file and creates a control char reference
        """

        def replace_symbolic(m):
            name = m.group(1)
            if name == '':
                return ':'
            if re.match('^[0-9]*$', name):
                return chr(int(name))
            if '.' in name:
                self.bitmap_preloaded.append(self.bitmap_img(name))
                return chr(len(self.bitmap_preloaded) - 1)
            if name in BITMAP_NAMED:
                return BITMAP_NAMED[name][2]
            # Unknown icon name - treat as literal text
            return ':' + name + ':'

        text = re.sub(r':([^:]*):', replace_symbolic, text)

        buf = array('B')
        cols = 0
        for c in text:
            (b, n) = self.bitmap_char(c)
            buf.extend(b)
            cols += n
        return buf, cols

    @staticmethod
    def bitmap_img(file):
        """
        Load an image file and return bitmap data.

        Returns: tuple of (buffer, length_in_byte_columns)

        Requirements:
        - Image must be 11 pixels high
        - 8-bit grayscale or color (color converted to grayscale)
        - Width padded to multiple of 8 pixels
        """
        try:
            from PIL import Image
        except ImportError:
            print("If you like to use images, the module pillow is needed. Try:")
            print("$ pip install pillow")
            raise

        im = Image.open(file)
        print("fetching bitmap from file %s -> (%d x %d)" % (file, im.width, im.height))
        if im.height != 11:
            raise ValueError("%s: image height must be 11px. Seen %d" % (file, im.height))

        buf = array('B')
        cols = int((im.width + 7) / 8)
        for col in range(cols):
            for row in range(11):
                byte_val = 0
                for bit in range(8):
                    bit_val = 0
                    x = 8 * col + bit
                    if x < im.width and row < im.height:
                        pixel_color = im.getpixel((x, row))
                        if isinstance(pixel_color, tuple):
                            monochrome_color = sum(pixel_color[:3]) / len(pixel_color[:3])
                        elif isinstance(pixel_color, int):
                            monochrome_color = pixel_color
                        else:
                            raise ValueError("%s: Unknown pixel format detected (%s)!" % (file, pixel_color))
                        if monochrome_color > 127:
                            bit_val = 1 << (7 - bit)
                        byte_val += bit_val
                buf.append(byte_val)
        im.close()
        return buf, cols

    def bitmap(self, arg):
        """
        Process argument as either a file path or text with :icon: syntax.

        If arg is an existing file path, load it as an image.
        Otherwise, treat it as text with :icon: syntax.
        """
        import os
        if os.path.exists(arg):
            return self.bitmap_img(arg)
        return self.parse_text(arg)


