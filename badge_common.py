def bytes_to_hex_string(byte_array):
    """Convert a byte array to a hex string (uppercase, no separators)."""
    return ''.join(f'{b:02X}' for b in byte_array)


def char_to_hex_string(char):
    """
    Convert a single character to its hex-encoded bitmap representation.

    Returns the CHAR_CODES format hex string for use in bt-magic.py
    """
    parser = BadgeTextParser()
    buf, cols = parser.bitmap_char(char)
    return bytes_to_hex_string(buf)


def text_to_hex_strings(text):
    """
    Convert text (with :icon: syntax) to list of hex strings per character.

    This is a convenience function for bt-magic.py compatibility.
    Note: This loses multi-column icon information, so for full icon support,
    use BadgeTextParser.parse_text() directly and convert the result.
    """
    parser = BadgeTextParser()
    buf, cols = parser.parse_text(text)
    # Return as single hex string for the entire message
    return bytes_to_hex_string(buf)


def get_icon_hex_data(icon_name):
    """
    Get the hex-encoded bitmap data for a builtin icon.

    Returns: hex string of the icon's bitmap data
    """
    if icon_name not in BITMAP_NAMED:
        raise ValueError(f"Unknown icon: {icon_name}")

    bitmap_data = BITMAP_NAMED[icon_name][0]
    return bytes_to_hex_string(bitmap_data)
